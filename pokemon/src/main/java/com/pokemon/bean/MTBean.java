package com.pokemon.bean;

public class MTBean {
	private String idMT;
	private String nombreMT;
	
	public MTBean() {
	}

	public MTBean(String idMT, String nombreMT) {
		this.idMT = idMT;
		this.nombreMT = nombreMT;
	}

	public String getIdMT() {
		return idMT;
	}

	public void setIdMT(String idMT) {
		this.idMT = idMT;
	}

	public String getNombreMT() {
		return nombreMT;
	}

	public void setNombreMT(String nombreMT) {
		this.nombreMT = nombreMT;
	}
	
	


}
